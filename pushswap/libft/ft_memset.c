#include <unistd.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*e;

	e = b;
	i = 0;
	while (i < len)
	{
		e[i] = c;
		i++;
	}
	return (b);
}
