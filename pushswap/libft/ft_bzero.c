#include <unistd.h>

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*e;

	e = s;
	i = 0;
	while (i < n)
	{
		e[i] = 0;
		i++;
	}
	return (s);
}
